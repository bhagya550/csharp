namespace ConsoleApp2
{
    class Program
    {
        static void Main(string[] args)
        {
            // wap to print no from 0 to 010 using while loop
            int i, j;
            for (i = 10; i <= 10; i++)
            {
                for (j = 10; j < i; j++)
                {
                    Console.Write("0" + "\t");
                }
                Console.WriteLine();

            }
            Console.ReadLine();

        }
    }
}
